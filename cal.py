num1=int(input("enter number"))
num2=int(input("enter number"))
symbol=input("enter symbol")
if symbol=="+":
    print(num1+num2,"addition")
if symbol=="-":
    print(num1-num2,"subtraction")
elif symbol=="*":
    print(num1*num2,"multiplication")
elif symbol=="%":
    print(num1%num2,"modulous")
else:
    print("not defined")
















